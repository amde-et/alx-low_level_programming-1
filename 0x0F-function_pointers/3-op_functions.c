#include "3-calc.h"

/**
 *op_add-add the number
 *@a:int
 *@b:int
 *Return:int
 */
int op_add(int a, int b)
{
return (a + b);
}

/**
 *op_sub-subtract the number
 *@a:int
 *@b:int
 *Return:int
 */
int op_sub(int a, int b)
{
return (a - b);
}


/**
 *op_mul-mult the number
 *@a:int
 *@b:int
 *Return:int
 */
int op_mul(int a, int b)
{
return (a * b);
}



/**
 *op_div-add the number
 *@a:int
 *@b:int
 *Return:int
 */
int op_div(int a, int b)
{
return (a / b);
}



/**
 *op_mod-add the number
 *@a:int
 *@b:int
 *Return:int
 */
int op_mod(int a, int b)
{
return (a % b);
}
